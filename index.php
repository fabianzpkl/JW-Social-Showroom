                      //console.log(data); 
                      $.isLoading( "hide" );

                      $.isLoading({ text: "Su registro se ha completado con éxito, gracias por inscribirse" });
                      $('#contact_form')[0].reset();
                      setTimeout(function(){ $.isLoading( "hide" ); }, 3000);
                    }
                  });
              }else{
                //alert('no valido');
              }

          });


        });

