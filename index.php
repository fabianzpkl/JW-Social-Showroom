      <script type="text/javascript">
        $(document).on('ready',function(){

          $("#contact_form").validate({
            rules: {
              name_user: "required",
              email_user: {
                required: true,
                email: true
              },
              tel_user: {
                required: true,
                number: true
              }
            },
            messages: {
                name_user: "Por favor Ingrese su nombre",
                tel_user: {
                    required: "Por favor ingrese su teléfono",
                    number:  "El teléfono debe ser un valor númerico"
                },
                email_user: {
                    required: "Por favor ingrese su correo",
                    email:  "Por favor ingrese un correo válido"
                }
            }
            /*errorClass: "invalid",
            validClass: "success",
            errorPlacement: function(error, element) {}*/
          });

          $(document).on('click','#send_form_social_show',function(e){
              e.preventDefault();
              if($('#contact_form').valid()){
                  //alert('si valido');
                  $.isLoading({ text: "Por favor espere, estamos procesando su inscripción..." });
                  $.ajax({
                     type: 'POST',
                     url: $('#contact_form').attr('action'),
                     data: $('#contact_form').serialize(),
                     success: function(data){
                      //console.log(data); 
                      $.isLoading( "hide" );

                      $.isLoading({ text: "Su registro se ha completado con éxito, gracias por inscribirse" });
                      $('#contact_form')[0].reset();
                      setTimeout(function(){ $.isLoading( "hide" ); }, 3000);
                    }
                  });
              }else{
                //alert('no valido');
              }

          });


        });

      </script>
